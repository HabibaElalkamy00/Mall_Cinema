function getOption() {         
    selectElement = document.querySelector('#exampleInputtofcinema');         
    output = selectElement.value; 
    if(output ==  "Ordinary Cinema"){
        document.querySelector('.output').value = '150 EGP';
    }else if (output ==  "Cars Cinema") {
        document.querySelector('.output').value = '400 EGP';
    }else if(output ==  "3D"){
        document.querySelector('.output').value = '350 EGP';
    }else if(output ==  "9D"){
        document.querySelector('.output').value = '500 EGP';
    }else if(output ==  "Bed Cinema"){
        document.querySelector('.output').value = '2000 EGP';
    }else if(output ==  "Private Cinema"){
        document.querySelector('.output').value = '5000 EGP';
    }
}  


